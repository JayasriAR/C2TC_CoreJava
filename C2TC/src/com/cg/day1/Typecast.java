package com.cg.dayone;

public class Typecast {
 public static void main(String[] args) {
	 int a=100;
	 System.out.println("Converting an int to char "+(char)a);
	 int d=20;
	 float e =d;
	 System.out.println("Converting an int to float "+e);
	 float b = 3.5f;
	 System.out.println("Converting an float to byte "+(byte)b);
	 char c = 'g';
	 System.out.println("Converting an char to int "+(int)c);
 }
}
